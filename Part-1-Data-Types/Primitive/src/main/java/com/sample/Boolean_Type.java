package com.sample;

/**
 *  Boolean Data Type
 *  it only have 2 values
 *  i.e. True or False
 *  its default value is false and size is 1 bit
 * */

public class Boolean_Type {
    public static void main(String[] args) {
        boolean flag = true;
        if(flag) {
            System.out.println("Its TRUE :: "+flag);
        } else {
            System.out.println("Its FALSE :: "+flag);
        }
    }
}
